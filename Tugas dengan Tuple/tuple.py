# a. Menampilkan isi tuple dan mengkonversi ke string
my_tuple = ('g', 'a', 'n', 't', 'e', 'n', 'g')
print("Tuple Original:", my_tuple)
print(''.join(my_tuple))  # Gabungkan jadi string

# b. Menampilkan nilai maksimum dari tuple
tuple_angka = (0, 200, 1000, 2, 3, 9000, 10)
print("nilai dari tuple:", tuple_angka)
print("nilai maksimum dari tuple:", max(tuple_angka))

# c. Menampilkan nilai minimum dari tuple
tuple_angka = (0, 200, 1000, 2, 3, 9000, 10)
print("nilai dari tuple:", tuple_angka)
print("nilai minimum dari tuple:", min(tuple_angka))
