# a. Menampilkan nilai maksimum dari list
list_angka = [456, 700, 200, 1000, 2000]
print("nilai dari list:", list_angka)
print("nilai maksimum dari list:", max(list_angka))

# b. Menampilkan nilai minimum dari list
list_angka = [456, 700, 200, 1000, 2000]
print("nilai dari list:", list_angka)
print("nilai minimum dari list:", min(list_angka))

# c. Menambahkan anggota baru ke dalam list
list_huruf = ['a', 'e', 'i', 'u']
print("Original List:", list_huruf)
list_huruf.insert(1, 'o')  # Menambahkan 'o' di indeks ke-1
print("Memasukkan huruf o pada list:", list_huruf)
